                <div class="pregunta">
                    <a class="consulta">1. ¿Dónde puedo ver el detalle completo de mis coberturas?<span class="icon-chevron-bottom iconon"></span></a>
                    <div class="respuesta">
                        <p>Puedes ingresar a nuestra web <a href="https://www.rimac.com.pe/PORTALWEB/" target="_blank"><b>https://www.rimac.com.pe/PORTALWEB/</b></a> , donde podrás ver el detalle de todos tus seguros Rimac. También puedes ver <a href="uploads/Detalle_Coberturas_Viajes.pdf" target="_blank"><b>aquí</b></a> el detalle de las coberturas generales de nuestros seguros de viaje.
 
                        </p>
                    </div>
                </div>
                <div class="pregunta">
                    <a class="consulta">2. ¿Cuál es la edad límite para adquirir este seguro?<span class="icon-chevron-bottom iconon"></span></a>
                    <div class="respuesta">
                        <p>Si eres titular la edad   mínimo es  18 años. Podemos asegurar hasta los 75 años con 364 días (antes de cumplir los 76).
                        </p>
                    </div>
                </div>
                <div class="pregunta">
                    <a class="consulta">3. ¿Cuál es el tiempo máximo que me puedo asegurar?<span class="icon-chevron-bottom iconon"></span>

                    </a>
                    <div class="respuesta">
                        <p>Nuestro seguro tiene una cobertura mínima de 3 días y máxima de 45 días y debe ser adquirido antes de salir de nuestro país.
                        </p>
                    </div>
                </div>
                <div class="pregunta">
                    <a class="consulta">4. ¿Cuál es la diferencia entre el Plan Individual y el Plan Familiar?<span class="icon-chevron-bottom iconon"></span>

                    </a>
                    <div class="respuesta">
                        <p>En el plan <b>INDIVIDUAL</b> solo se puede asegurar a 1 persona (Asegurado Titular) el cual goza de todas las Asistencias (atención médica por accidente o enfermedad, gastos por convalecencia, retraso de viaje, pérdida de equipaje, cancelación de viaje por accidente, regresos anticipados) y Coberturas (muerte accidental e invalidez).
En el plan <b>FAMILIAR</b> tienes la opción de asegurarte a ti + Cónyuge (o cualquier otro acompañante adulto) + Hijos (máximo 4), en este plan todo el grupo familiar goza de las Asistencias pero solo el Asegurado Titular tiene las Coberturas de muerte accidental e invalidez. En este plan los niños menores de 12 años no pagan (máximo 2).
                        </p>
                    </div>
                </div>
                <div class="pregunta">
                    <a class="consulta">5. ¿Qué Planes puedo adquirir en caso viaje a países del espacio Schengen?<span class="icon-chevron-bottom iconon"></span>

                    </a>
                    <div class="respuesta">
                        <p>
                            Para ingresar a los países que forman parte del espacio Schengen es requisito indispensable contar con un seguro que cubra como mínimo $ 35,000 en gastos médicos. Tenemos 3 planes ideales: <b>TURISTA, VIP y PLATINO.</b>
                        </p>
                    </div>
                </div>

                <div class="pregunta">
                    <a class="consulta">6. ¿Qué hago si tengo alguna duda sobre alguna de mis coberturas?<span class="icon-chevron-bottom iconon"></span>

                    </a>
                    <div class="respuesta">
                        <p>
                           Antes del viaje puedes llamarnos al <a href="tel:4111111"><b>411-1111</b></a> opción 5. Si ya estás de viaje y necesitas hacer una consulta o uso de tu seguro, llámanos al <a href="tel:5125010"><b>(511) 5125010*.</b></a>
                        </p>
                    </div>
                </div>
            </div>


            <div class="preguntas siniestros  col-md-8" style="display: none;">
                                                    <h1 class="title">Preguntas frecuentes</h2>

                <div class="pregunta">
                    <a class="consulta">1. Asegurado
                                         <span class="icon-chevron-bottom iconon"></span>
</a>
                    <div class="respuesta">
                        <p>Persona natural cuya integridad física se asegura en la póliza, quien debe cumplir con los requisitos de edad y condiciones de asegurabilidad establecidos en las Condiciones Particulares.
 
                        </p>
                    </div>
                </div>
                <div class="pregunta">
                    <a class="consulta">2. Beneficiario
                                         <span class="icon-chevron-bottom iconon"></span>
</a>
                    <div class="respuesta">
                        <p>Persona natural que recibirá la indemnización, beneficio o suma asegurada que se establezca en las Condiciones Particulares de la póliza. El beneficiario siempre será el Herededo Legal.
                        </p>
                    </div>
                </div>
                <div class="pregunta">
                    <a class="consulta">3. Cobertura
                                             <span class="icon-chevron-bottom iconon"></span>

                    </a>
                    <div class="respuesta">
                        <p>Es todo aquello frente a lo cual tu seguro se compromete a protegerte. Estos compromisos están listados en tu contrato (póliza de seguros).
                        </p>
                    </div>
                </div>
                <div class="pregunta">
                    <a class="consulta">4. Vigencia<span class="icon-chevron-bottom iconon"></span>

                    </a>
                    <div class="respuesta">
                        <p>La vigencia de la póliza se inicia a las 00.00 horas y finaliza a las 23.59 horas de los días establecidos en las Condiciones Particulares de la póliza.
                        </p>
                    </div>
                </div>
                <div class="pregunta">
                    <a class="consulta">5. Exclusiones<span class="icon-chevron-bottom iconon"></span>

                    </a>
                    <div class="respuesta">
                        <p>
                            Son situaciones no cubiertas por el seguro y que están detalladas en la póliza. Algunos ejemplos de exclusiones son malas prácticas o actos ilegales.
                            <br>
                            Lee bien tu póliza para conocer y entender las exclusiones de tu seguro.
                        </p>
                    </div>
                </div>

                 <div class="pregunta">
                    <a class="consulta">6.  Prima<span class="icon-chevron-bottom iconon"></span>

                    </a>
                    <div class="respuesta">
                        <p>
                            Es el pago que realizas a cambio de estar protegido por un seguro. Puede ser pagada en cuotas mensuales, semestrales o anuales dependiendo de lo que esté establecido en tu póliza de seguro.
                        </p>
                    </div>
                </div>

                
                <div class="pregunta">
                    <a class="consulta">7.  Siniestro<span class="icon-chevron-bottom iconon"></span>

                    </a>
                    <div class="respuesta">
                        <p>
                            Son todos los accidentes/inconvenientes que están contemplados en tu póliza de seguros. Un siniestro no es el peligro de que algo pase y tampoco es cualquier desastre. Para que algo sea considerado un siniestro debe estar contemplado como tal en tu póliza de seguros, de no ser así, solo será un acontecimiento negativo, pero no un siniestro.
                        </p>
                    </div>
                </div>


